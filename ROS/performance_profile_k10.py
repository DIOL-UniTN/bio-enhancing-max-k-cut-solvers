def performance_profile(data, tau):
    best = np.max(data, axis=1)
    ratios = best[:, None] / data
    return [(ratios[:, i] <= tau).mean() for i in range(data.shape[1])]

taus = np.linspace(1.0, 1.1, 200)

plt.figure(figsize=(8,5))
for i, solver in enumerate(solvers):
    rho = [performance_profile(data, t)[i] for t in taus]
    plt.plot(taus, rho, label=solver)

plt.xlabel(r"$\tau$")
plt.ylabel(r"$\rho(\tau)$")
plt.title("Dolan–Moré Performance Profiles (Max-10-Cut, k=10)")
plt.legend()
plt.grid(True, linestyle="--", alpha=0.6)
